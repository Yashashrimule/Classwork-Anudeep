import java.util.*;
class EvenOdd
{
  public static void main(String args[])
  {
    int num;
    Scanner sc=new Scanner(System.in);
    System.out.println("Enter any number :");
    num=sc.nextInt();
    if(num%2==0)
    {
      System.out.println("The number is EVEN");
    }
     else
     {
       System.out.println("The number is ODD");
     }
 }
}
      
      
    